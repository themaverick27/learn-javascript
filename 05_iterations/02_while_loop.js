// while loop

// example-1
let index = 1
while(index <= 10){
    //console.log(`value of index is ${index}`);

    index = index + 2;
}

// example-2
let myArr = ["flash", "batman", "superman"]

let ind = 0
while(ind < myArr.length){
    // console.log(`value is ${myArr[ind]}`);

    ind = ind + 1;
}


// do-while loop - runs atleast for one time
let score = 11
do {
    console.log(`score is: ${score}`);

    score++;
} while (score <= 10);